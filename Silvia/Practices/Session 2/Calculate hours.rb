#Script to calculate seconds, minutes, hours
time_in_secs = 3600.0
puts "The time in seconds is #{time_in_secs}"

time_in_mins = time_in_secs / 60
puts "The time in minutes is #{time_in_mins}"

time_in_hours = time_in_mins / 60
puts "The time in hours is #{time_in_hours}"

time_in_days = time_in_hours / 24
puts "The time in days is #{time_in_days}"